#include<iostream>
using namespace std;

int main(){
    /* Arithmetics Operators +,-,/,* " */

    int a = 10;
    int b = 20;
    cout<<"Sum: "<<a+b<<endl;
    cout<<"Sub: "<<a-b<<endl;
    cout<<"Division: "<<30.0/b<<endl;
    cout<<"Multiply: "<<a*b<<endl;

    /* Relation Operators */

    bool first = (a<b);
    cout<<first<<endl;

    bool second = (a>b);
    cout<<second<<endl;

    bool third = (a==b);
    cout<<third<<endl;

    bool fourth = (a!=b);
    cout<<fourth<<endl;


    /* Logical Operators */

   // && , || , !
    cout<<"OR: "<<!a;
}